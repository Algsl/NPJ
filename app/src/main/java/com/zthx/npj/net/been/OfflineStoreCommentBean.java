package com.zthx.npj.net.been;

public class OfflineStoreCommentBean {
    private String user_id;
    private String token;
    private String store_id;
    private String content;
    private String goods_star;
    private String service_star;
    private String logistics_str;

    public String getUser_id() {
        return user_id;
    }

    public void setUser_id(String user_id) {
        this.user_id = user_id;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public String getStore_id() {
        return store_id;
    }

    public void setStore_id(String store_id) {
        this.store_id = store_id;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getGoods_star() {
        return goods_star;
    }

    public void setGoods_star(String goods_star) {
        this.goods_star = goods_star;
    }

    public String getService_star() {
        return service_star;
    }

    public void setService_star(String service_star) {
        this.service_star = service_star;
    }

    public String getLogistics_str() {
        return logistics_str;
    }

    public void setLogistics_str(String logistics_str) {
        this.logistics_str = logistics_str;
    }
}
